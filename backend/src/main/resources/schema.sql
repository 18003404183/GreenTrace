CREATE TABLE IF NOT EXISTS user_accounts (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(50) NOT NULL UNIQUE,
    password VARCHAR(80) NOT NULL,
    real_name VARCHAR(50) NOT NULL,
    role VARCHAR(30) NOT NULL,
    area_name VARCHAR(80),
    status VARCHAR(20) NOT NULL
);

CREATE TABLE IF NOT EXISTS areas (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(80) NOT NULL,
    code VARCHAR(40) NOT NULL UNIQUE,
    parent_id BIGINT,
    manager VARCHAR(50),
    risk_level VARCHAR(20) NOT NULL,
    address VARCHAR(160)
);

CREATE TABLE IF NOT EXISTS devices (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    code VARCHAR(60) NOT NULL UNIQUE,
    type VARCHAR(40) NOT NULL,
    area_name VARCHAR(80) NOT NULL,
    status VARCHAR(20) NOT NULL,
    last_online VARCHAR(30),
    pm25 DECIMAL(10, 2),
    voc DECIMAL(10, 2)
);

CREATE TABLE IF NOT EXISTS device_readings (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    device_id BIGINT NOT NULL,
    metric_name VARCHAR(40) NOT NULL,
    metric_value DECIMAL(10, 2) NOT NULL,
    unit VARCHAR(20) NOT NULL,
    recorded_at VARCHAR(30) NOT NULL
);

CREATE TABLE IF NOT EXISTS reports (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    reporter_name VARCHAR(50) NOT NULL,
    phone VARCHAR(30),
    area_name VARCHAR(80) NOT NULL,
    location VARCHAR(160) NOT NULL,
    category VARCHAR(40) NOT NULL,
    description VARCHAR(500) NOT NULL,
    status VARCHAR(20) NOT NULL,
    source VARCHAR(30) NOT NULL,
    create_time VARCHAR(30) NOT NULL,
    handler VARCHAR(50)
);

CREATE TABLE IF NOT EXISTS report_records (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    report_id BIGINT NOT NULL,
    action VARCHAR(40) NOT NULL,
    operator VARCHAR(50) NOT NULL,
    remark VARCHAR(300) NOT NULL,
    created_at VARCHAR(30) NOT NULL
);

CREATE TABLE IF NOT EXISTS supervision_tasks (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    task_no VARCHAR(40) NOT NULL UNIQUE,
    report_id BIGINT NOT NULL,
    title VARCHAR(120) NOT NULL,
    assignee VARCHAR(50) NOT NULL,
    deadline VARCHAR(30) NOT NULL,
    priority VARCHAR(20) NOT NULL,
    status VARCHAR(20) NOT NULL,
    created_at VARCHAR(30) NOT NULL,
    completed_at VARCHAR(30)
);

CREATE TABLE IF NOT EXISTS warnings (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(120) NOT NULL,
    level VARCHAR(20) NOT NULL,
    area_name VARCHAR(80) NOT NULL,
    source_type VARCHAR(40) NOT NULL,
    status VARCHAR(20) NOT NULL,
    created_at VARCHAR(30) NOT NULL,
    advice VARCHAR(300)
);

CREATE TABLE IF NOT EXISTS warning_rules (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    metric_name VARCHAR(40) NOT NULL,
    operator VARCHAR(10) NOT NULL,
    threshold DECIMAL(10, 2) NOT NULL,
    level VARCHAR(20) NOT NULL,
    enabled BOOLEAN NOT NULL,
    advice VARCHAR(300)
);

CREATE TABLE IF NOT EXISTS announcements (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(120) NOT NULL,
    content VARCHAR(500) NOT NULL,
    published_at VARCHAR(30) NOT NULL
);

CREATE TABLE IF NOT EXISTS operation_logs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    operator VARCHAR(50) NOT NULL,
    module VARCHAR(50) NOT NULL,
    action VARCHAR(80) NOT NULL,
    target VARCHAR(160),
    detail VARCHAR(400),
    created_at VARCHAR(30) NOT NULL
);

CREATE TABLE IF NOT EXISTS attachments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    report_id BIGINT NOT NULL,
    record_id BIGINT,
    file_name VARCHAR(180) NOT NULL,
    file_path VARCHAR(300) NOT NULL,
    content_type VARCHAR(80) NOT NULL,
    usage_type VARCHAR(30) NOT NULL,
    uploaded_by VARCHAR(50) NOT NULL,
    uploaded_at VARCHAR(30) NOT NULL
);

CREATE TABLE IF NOT EXISTS map_points (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    point_type VARCHAR(30) NOT NULL,
    area_name VARCHAR(80) NOT NULL,
    status VARCHAR(30) NOT NULL,
    risk_level VARCHAR(20) NOT NULL,
    x_percent DECIMAL(6, 2) NOT NULL,
    y_percent DECIMAL(6, 2) NOT NULL,
    description VARCHAR(300)
);

CREATE TABLE IF NOT EXISTS notifications (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(120) NOT NULL,
    content VARCHAR(400) NOT NULL,
    type VARCHAR(30) NOT NULL,
    read_flag BOOLEAN NOT NULL,
    created_at VARCHAR(30) NOT NULL
);
